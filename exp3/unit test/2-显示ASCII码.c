// 描述
//     由键盘输入一个字符，以十进制整数输出其对应的ASCII码
// 输入格式
//     输入一个字符
// 输出格式
//     对应的ASCII码（十进制形式）
// 输入样例
//     A
// 输出样例
//     65

#include <stdio.h>

int main(int argc, char *argv[]) {
    char ch = 0;
    scanf("%c", &ch);
    printf("%d", ch);
}
